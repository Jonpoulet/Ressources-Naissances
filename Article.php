		<a href="#">Les inquiétudes des nouvelles mamans</a></br>
		<reference>Publié le 14 février 1995 par Mère-Contact, ancien nom pour Ressources-Naissances</reference>
	</div>
	
	<div class="desc"><tgauche>
		<P align=left><STRONG><FONT size=2><U>Les inquiétudes des nouvelles mamans</U></FONT></STRONG></P>
		<P align=left><FONT size=2>Nous souhaitons toutes avant la naissance de notre premier bébé être une mère calme et un peu portée à l'inquiétude.&nbsp; Et pourtant, la plupart des mères se font du souci face à leur nouveau-né; l'attention se porte anxieusement sur les petites rougeurs, les gémissements, la couleur des selles, etc.&nbsp; Souvent, on exagère l'importance des symptômes.</FONT></P>
		<P align=left><FONT size=2>Qu'une mère soit inquiète pour son bébé, ceci est une réaction tout à fait normale qui révèle son instinct de protection et l'attachement qu'elle porte à l'enfant.&nbsp; L'anxiété et l'inquiétude sont des sentiments rattachés à la première étape d'un apprentissage à faire; c'est aussi la manifestation de notre mobilisation émotive pour une tâche qui nous tient à coeur.&nbsp; La plupart des mères vivent ces sentiments sur une courte période, car peu à peu au fil des jours, elles acquièrent&nbsp;plus de confiance.&nbsp; C'est un peu comme lorsque l'on débute un nouvel emploi; il faut se donner le temps d'apprendre et de s'adapter avant d'espérer pouvoir travailler avec calme et sénérité.</FONT></P>
		<P align=left><FONT size=2>Évidemment, cela dépend du tempérament de chacune.&nbsp;&nbsp;On sait que certaines personnes sont plus perfectionnistes, s'attachent aux détails davantage ou&nbsp;doutent plus facilement d'elles-mêmes.&nbsp; Beaucoup de mères avouent avoir été très émotives et se sentir incompétentes face au nouveau rôle.</FONT></P>
		<P align=left><FONT size=2>Toutefois, l'inquiétude n'est pas typiquement féminin.&nbsp; Il arrive que la mère soit plutôt calme et que ce soit le père qui est pris par l'inquiétude.&nbsp; Aujourd'hui, les pères ont tendance à s'impliquer plus tôt pour les soins du bébé, ce qui les expose aussi à l'inquiétude.</FONT></P>
		<P align=left><FONT size=2>Le remède à l'inquiétude, c'est l'expérience.&nbsp; Au deuxième ou troisième bébé, les parents savent qu'un bébé, ce n'est pas aussi fragile qu'il en a l'air; ils savent aussi qu'il peut survivre même s'ils font des erreurs.</FONT></P>
