	<div class="titre">
		<a href="#">Les troubles de l'humeur du post-partum</a></br>
		<reference>Publié le 1 janvier 2005 par Ressources-Naissances</reference>
	</div>
	
	<div class="desc"><tgauche>
	
		<P>La grossesse du point de vue de la médecine psychologique est l'événement le plus complexe de la vie humaine. – Ian Brockington</P>
		<P>La grossesse étant une phase de développement, donc une crise d'identité, les enjeux biopsychosociaux sont autant de facteurs de risque de rechute, d'aggravation ou de déclenchement d'un trouble psychique.</P>
		<P><STRONG>LES TROUBLES AFFECTIFS DU POST-PARTUM PASSENT ENCORE SOUS SILENCE. POURQUOI ?</STRONG></P>
		<UL>
		<LI>La période d'ajustement dite "normale" à la condition parentale comporte certains traits pouvant se révéler difficiles à distinguer des premiers symptômes reliés aux troubles affectifs (angoisse, insomnie, pleurs, fatigue, sentiment d'être en mauvaise santé). 
		<LI>Les femmes ne sont pas toujours disposées à en parler autour d'elles. Censées être heureuses et reconnaissantes, elles vivent embarras, regret, honte, culpabilité… 
		<LI>Les futurs parents sont bien informés des caractéristiques physiques de l'accouchement, mais peu préparés aux défis affectifs et aux facteurs de stress qui les attendent. Face à leur désarroi, ils concluent à une réaction inhabituelle et remettent en question leur capacité parentale. 
		<LI>Souvent les perturbations ne sont pas prises en compte par les proches et les professionnels et sont attribuées uniquement à un phénomène hormonal ou à une certaine faiblesse propre à la femme. La femme associe donc ses réactions à son nouveau rôle et a tendance à en minimiser la gravité ou même à les nier.</LI></UL>
		<P><STRONG>1. Quelques statistiques</STRONG></P>
		<UL>
		<LI>Une (1) femme sur 10 souffrira de dépression durant sa grossesse, 1 sur 8 en post-partum. 
		<LI>Le taux de suicide est 70 fois plus élevé dans l'année qui suit l'accouchement que dans tout autre période de la vie des femmes (Appleby L. et al. 1998). 
		<LI>La première cause de mortalité maternelle est le suicide.</LI></UL>
		<P><STRONG>2. Mythe de la maternité</STRONG></P>
		<UL>
		<LI>La période post-partum est un temps exclusivement positif dans la vie d'une femme. 
		<LI>Toutes les mères savent instinctivement comment prendre soin de leurs bébés. 
		<LI>Les mères ne ressentent que de l'amour pour leurs enfants et se sentent attachées à leurs enfants dès la naissance. 
		<LI>Être mère est une expérience de vie totalement satisfaisante pour toutes les femmes.</LI></UL>
		<P><STRONG>3. Les enjeux de la grossesse…</STRONG></P>
		<P>D'hier… à aujourd'hui...</P>
		<P>HIER...<BR>Les complications obstétricales les plus fréquentes étaient physiques. <BR>AUJOURD'HUI...<BR>Les complications obstétricales les plus fréquentes sont maintenant psychiatriques. * </P>
		<P>HIER...<BR>Familles très nombreuses donc un réseau d'aide bien organisé dans la communauté. <BR>AUJOURD'HUI...<BR>Familles peu nombreuses, le réseau naturel est surchargé par le travail et sa propre famille, donc beaucoup moins de support pour les nouvelles mamans.</P>
		<P>HIER...<BR>L'apprentissage du rôle de parent se transmettait de génération en génération. <BR>AUJOURD'HUI...<BR>Valorisation douteuse du rôle de parent.</P>
		<P>HIER...<BR>Rôles parentaux bien définis. <BR>AUJOURD'HUI...<BR>Conditions de travail souvent précaires = stress élevé.<BR>Les idéaux de performance : la meilleure grossesse, le meilleur accouchement, le meilleur bébé, le plus beau des allaitements, un post-partum heureux…</P>
		<P><STRONG>4. Les troubles de l'humeur du post-partum</STRONG></P>
		<P>Il existe 3 troubles de l'humeur du post-partum soit : les blues, la dépression et la psychose du post-partum.</P>
		<P>a) Les blues du post-partum (50 à 80% des femmes)</P>
		<P>Par définition, les blues du post-partum est un phénomène transitoire, bénin et normal qui apparaît TOUJOURS à l'intérieur des 2 premières semaines après l'accouchement. Tous les troubles de l'humeur qui débordent cette limite ne sont plus des baby blues. </P>
		<P>Ils se définissent par une «abondance émotionnelle» causée en grande partie par la chute d'hormones après l'accouchement. On pleure mais on ne sait pas pourquoi ! Quand on sait que le taux d'œstrogène passe à du 1000 pour 1 en 24 heures, on peut comprendre pourquoi… On peut y vivre aussi : peurs, angoisses, irritabilité, insomnie, manque de concentration, variabilité émotionnelle.</P>
		<P>b) La dépression du post-partum (10 à 20% des femmes)</P>
		<UL>
		<LI>Si les blues durent plus de 2 semaines et perturbent les activités quotidiennes. 
		<LI>Si les sentiments négatifs tendent à prendre le dessus sur les positifs, même si on arrive à fonctionner relativement bien. 
		<LI>Peut aussi survenir n'importe quand au cours de la 1ère année suivant la naissance, même après une période sans symptômes.</LI></UL>
		<P>Les symptômes de la dépression post-partum peuvent donc se manifester dans les 24 heures ou jusqu'à un an après l'accouchement. Ils sont les mêmes que la dépression majeure :</P>
		<UL>
		<LI>une humeur dépressive ou une diminution marquée de goût et d'intérêt 
		<LI>insomnie ou hypersomnie 
		<LI>agitation ou ralentissement psychomoteur 
		<LI>fatigue ou perte d'énergie 
		<LI>sentiments de désespoir, d'incompétence ou de culpabilité excessive 
		<LI>troubles d'attention et de concentration 
		<LI>pensées récurrentes de mort 
		<LI>Autres : peurs, perte ou gain de poids significatif, problèmes physiques, irritabilité, crises d'angoisse et de panique (avec ou sans autres symptômes), troubles obsesso-compulsifs, se sentir effrayée à l'idée de faire mal à son bébé, pensées récurrentes de mort. 
		<LI>un diagnostic et un suivi doivent être envisagés.</LI></UL>
		<P>c) Psychose du post-partum (0,2% = 2 sur 1000)</P>
		<UL>
		<LI>Survient rapidement et soudainement, en général dans les 3 semaines suivant l'accouchement. 
		<LI>Troubles de pensées, incohérence, hallucinations, délires, extrême confusion. 
		<LI>La psychose est très rare, mais doit être traitée en toute urgence. </LI></UL>
		<P><STRONG>5. Je suis plus à risque si…</STRONG></P>
		<UL>
		<LI>J'ai des antécédents de dépression du post-partum (50% de risque). 
		<LI>J'ai fait une dépression majeure durant ma grossesse (35% de risque) 
		<LI>J'ai des antécédents de dépression majeure (25% de risque) 
		<LI>Le risque pour la population en générale est d'environ 12% ² 
		<LI>Antécédents de troubles mentaux chez la femme et sa famille 
		<LI>Antécédents de dépression postnatale chez la femme et sa famille 
		<LI>Difficultés conjugales 
		<LI>Manque de soutien social, isolement 
		<LI>Bébé nécessitant beaucoup de soins 
		<LI>Nombreux événements stressants (mariage, deuil, maladie, déménagement, changement d'emploi…) dans les 2 années précédant la naissance ou l'adoption d'un enfant 
		<LI>Peurs, doutes et sentiments ambivalents face à la grossesse 
		<LI>Grossesse ou accouchement difficile et/ou traumatisant 
		<LI>Manque d'estime de soi 
		<LI>Attentes irréalistes face à la maternité 
		<LI>Peu ou pas d'expérience avec les enfants avant l'accouchement 
		<LI>Événements passés non résolus, antécédents de sévices corporels et sexuels 
		<LI>Certaines caractéristiques de personnalité (perfectionniste, sensible aux changements, humeur changeante…)</LI></UL>
		<P><STRONG>6. Les traitements possibles</STRONG></P>
		<UL>
		<LI>Psychothérapie individuelle ou familiale/conjugale 
		<LI>Alimentation, exercice, sommeil 
		<LI>Participation à un groupe d'entraide 
		<LI>Prise de médicaments </LI></UL>
		<P>Non traités, les troubles affectifs majeurs du post-partum peuvent avoir des conséquences sur la relation mère/enfant et sur le développement même de l'enfant.</P>
		<P><STRONG>7. Je demande de l'aide à…</STRONG></P>
		<UL>
		<LI>Mon réseau naturel 
		<LI>L'infirmière du CLSC venue me visiter après mon accouchement 
		<LI>Mon médecin de famille 
		<LI>L'urgence de l'hôpital 
		<LI>Ressources-Naissances : Service à domicile (relevailles), café-rencontre et autres services. </LI></UL>
