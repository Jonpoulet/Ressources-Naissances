		L’allaitement en trio !
 
		<p>J’ai le goût de vous raconter la petite histoire de ma famille qui a vécu l’allaitement en trio pendant plus d’un an… 
		Non, je n’allaite pas mes trois enfants en même temps, même si ça pourrait bien se faire… 
		Je vous parle plutôt de mes deux sœurs et de moi-même qui avons allaité toutes trois pendant plus d’un an en même temps !
		
		<p>Lorsque j’étais enceinte de mon petit dernier et que ma grossesse tirait à sa fin, 
		j’ai eu la surprise d’apprendre que mes deux sœurs étaient devenues enceintes elles aussi. 
		Elles attendaient leur bébé pour le mois d’août toutes les deux !
		
		<p>Ma sœur cadette, en serait à son troisième allaitement, je n’étais pas inquiète pour elle ! 
		Quant à ma sœur aînée, elle allait connaître l’allaitement pour la première fois et je savais qu’elle avait très hâte !  
		Nous étions deux depuis quelques années à lui donner le goût de vivre ce lien intense avec son enfant et elle était déjà 
		tout à fait convaincue des bienfaits de l’allaitement tant pour son bébé que pour elle-même.
		
		<p>Le mois d’août est enfin arrivé. De mon côté, j’allaitais mon fils depuis maintenant six mois. 
		Ma jeune sœur a eu un troisième garçon qu’elle a évidemment allaité et notre « grande » sœur a eu sa petite fille !  
		Ce fut tout de suite le début d’une belle relation entre elle et son bébé mais aussi entre elle et nous, ses deux jeunes sœurs ! 
		Ce qui est merveilleux, c’est qu’elle nous appelait tous les jours pour nous poser des questions ou simplement pour nous 
		dire son bonheur d’avoir son enfant dans ses bras !  Il faut dire qu’elle habite à Montréal, alors que ma sœur cadette et moi habitons à Lévis. 
		Il nous a donc fallu près d’un mois avant de voir ce petit bout de femme !  En attendant, les contacts téléphoniques étaient fréquents et
		 nous étions heureuses de donner ce support à notre sœur.
		 
		 <p>Notre premier allaitement en trio s’est vécu chez nos parents lors de la première visite de notre sœur. 
		 Mes parents étaient très émus et heureux de voir ce bel accomplissement de leur progéniture !  
		 Nous étions là, sur le divan, toutes les trois avec un bébé au sein !
		 
		 <p>Depuis ce temps, nous avons allaité très souvent en même temps, car mes deux sœurs ont allaité longtemps elles aussi !  
		 Je me rends compte en en parlant autour de moi que je suis chanceuse que l’allaitement soit bien vu dans ma famille et
		  qu’en plus nous l’ayons vécu toutes en même temps.  Pour ajouter à cette complicité familiale, je peux vous dire aussi
		   que durant cette même année, j’ai trois cousines qui ont toutes allaité leur bébé plus de six mois !  
		   Devinez de quoi on parle dans nos « partys » de famille ???
		   
		  <p>Si vous êtes intéressée à vivre des moments semblables, 
		  les rencontres de la Ligue La Leche, ont lieu le 2e lundi 
		  de chaque mois à 19h au local de Ressources-Naissances.
		  
		  <p>Marie-Josée Noël, monitrice de la Ligue La Leche, Lévis
