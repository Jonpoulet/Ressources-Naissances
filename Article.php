		<a href="#">Les Relevailles : Un service essentiel pour toutes les mères qui ont accouché</a></br>
		<reference>Publié le 31 août 2006 par Lucie Thibodeau, Réseau des centres de ressources périnatales</reference>
	</div>		
		
	<div class="desc"><tgauche>
		<p><souligne>Les Relevailles : Un service essentiel pour toutes les mères qui ont accouché</souligne>

		<p><gras>Les relevailles : un mot ancien pour une réalité actuelle</gras>

		<p>Le mot relevailles est un mot ancien, vieilli même. 
		Dans le dictionnaire, il signifie le fait de se lever, 
		de relever de couches (Petit Robert) et la bénédiction donnée à une femme relevant de couches (Larousse). 
		Une femme en couches est aussi une expression qui n’est presque plus utilisée. 
		Cela signifie l’état d’une femme qui accouche ou vient d’accoucher (Larousse). 
		Les institutions publiques du Québec sont déconfessionnalisées et le langage a évolué, 
		mais les besoins des mères qui donnent naissance sont encore bien réels et même plus importants que jamais.
		 Réhabiliter ce mot, c’est mettre en perspective cette réalité bien actuelle. Qui peut croire qu’on puisse donner 
		la vie et rentrer chez soi comme si de rien n’était ?
		
		<p><gras>Les besoins des femmes qui ont accouché sont universels</gras>
		
		<p>Dans l’histoire des femmes du Québec, les relevailles étaient le soutien qu’apportait 
		la famille immédiate de la mère à celle-ci après la naissance de son enfant, pour lui permettre 
		de se reposer et de prendre un moment de répit des responsabilités familiales. Bien que l’organisation 
		et la structure familiale aient beaucoup changé en vingt ans et même si les femmes n’ont plus une dizaine
		 d’enfants, leurs besoins ne sont pas si différents d’avant, ni d’ailleurs dans le monde. 
		 Toutes ont besoin de repos, de prendre contact avec ce nouveau bébé, d’apprendre à répondre aux besoins 
		 e cet enfant et d’être entourées de personnes bienveillantes pour s’occuper de leurs besoins à elles. 
		 Porter un enfant durant neuf mois représente toujours une expérience hautement significative, exigeante 
		 physiquement et émotionnellement. Peu importe comment se déroule l’accouchement, se retrouver face à un 
		 petit être humain totalement différent de tous les autres et entièrement dépendant et être projetée du 
		 jour au lendemain dans un univers nouveau et inconnu n’est jamais banal. Toutes les mères ont besoin de
		  sentir qu’autour d’elles et leur bébé, un réseau existe et assure la poursuite de la vie active et des 
		  responsabilités familiales. Si la vie de ce nouveau-né commence au grand jour, la vie au grand jour de 
		  la mère prend une pause, qui est un moment très intense et très riche de découvertes. Cette période peut
		   aussi tourner au cauchemar, à l’épuisement et à la dépression si la nouvelle mère doit continuer à s’occuper 
		   de tout comme avant, si elle ne réussit pas à se reposer, si elle est anxieuse, si elle ou son bébé a des problèmes 
		   de santé. L’aide la plus efficace est celle qui prend soin de la dyade mère-enfant et facilite la présence du père 
		   autour de cette dyade. Y a-t-il des volontaires dans la famille et dans le milieu ?
		   
		   <p><gras>Aider une nouvelle mère est autant un art qu’un métier</gras>
		   
		   <p>Une grand-mère, une amie, une sœur, une collègue de travail peut donner un coup de main à la mère 
		   et lui offrir une aide concrète autant qu’un accompagnement chaleureux. Les ingrédients nécessaires 
		   pour être utile sont de la disponibilité, de l’ouverture, une bonne écoute, du respect des valeurs 
		   de la mère et de l’expérience comme mère ou à tout le moins une certaine expertise face aux besoins 
		   de la mère et du bébé. Souvent, les mères ne trouvent pas cette aide dans leur réseau pour une multitude 
		   de raisons : tout le monde est au travail, les personnes habitent trop loin, la mère est la première à 
		   avoir un bébé dans sa famille, personne ne semble être en mesure d’aider, sans juger, critiquer ou dire quoi faire.
		   
		   <p><gras>Les relevailles répondent à un besoin et à une demande</gras>
		   
		   <p>Le service de relevailles s’est développé dans les centres de ressources périnatales parce que les mères ont 
		   besoin de soutien et ne trouvent pas cette aide essentielle. Il s’agit d’une aide-maman, une aide natale ou une
		   assistante périnatale, autant de noms utilisés pour nommer une femme qui vient aider la mère à sa demande et en 
		   fonction de ses besoins. Elle peut bercer, cajoler, donner le bain ou réconforter bébé pendant que maman se repose. 
		   Elle peut s’occuper des autres enfants (jeux, bricolages, sorties à l’extérieur), préparer des collations, des repas, 
		   faire une brassée de lavage et compléter les tâches d’entretien quotidien. Elle est une oreille attentive qui écoute les 
		   joies comme les inquiétudes ou les déceptions, qui apporte des réponses aux questions (allaitement, alimentation, post partum, 
		   développement du bébé), qui donne de l’encouragement dans les apprentissages de parent et de l’information sur les ressources 
		   activités et services disponibles dans le milieu. C’est un coup de pouce précieux qui peut faire toute la différence.
		   Une femme peut demander une aide à domicile pour une multitude de raisons qui sont toutes importantes : sa santé est précaire, 
		   elle est fatiguée, elle a besoin de « prendre l’air », elle est une immigrante nouvellement arrivée, elle a des problèmes d’allaitement, 
		   elle est isolée et a besoin de parler, elle trouve difficile l’adaptation avec son bébé, l’aîné réagit fortement, elle a donné naissance
		    à des jumeaux, elle a besoin de répit.
		    
		    <p><gras>Le service de relevailles dans les centres de ressources périnatales est accessible</gras>
		    
		    <p>L’aide-maman reçoit les demandes de la mère qui décide de l’aide dont elle-même a besoin. L’aide-maman est aussi capable de décoder 
		    d’autres besoins et lui faire valider. Elle a reçu une formation pour répondre aux questions et aux demandes de la mère et s’ajuster 
		    dans le respect des valeurs et des choix de la mère. Elle est souvent elle-même mère Ce service permet à la mère d’avoir de l’aide et 
		    d’obtenir des réponses à ses questions sans même avoir à se déplacer ou quitter la chaleur de son domicile. Le service est offert sous 
		    forme de blocs de trois heures par semaine. La mère peut demander plus d’un bloc, surtout au début. La mère fait sa demande en exprimant 
		    autant qu’elle le peut ses besoins et ses attentes. L’aide-maman précise ce qu’elle peut offrir et ce qui ne fait pas partie de ses services 
		    (ex : grand ménage, soins de santé à la mère, soins spécialisés à un bébé handicapé). La mère contribue aux services reçus en fonction de 
		    sa capacité financière. Elle utilise le service aussi longtemps qu’elle le juge important pour récupérer de la fatigue des premières semaines, 
		    retrouver une énergie suffisante pour s’organiser, se sentir prête à continuer sa vie de famille.
		    
		    <p><gras>Les relevailles; ça change le monde de la mère!</gras>
		    
		    <p>Le service de relevailles a un effet important sur l’adaptation à la vie avec bébé et la récupération après l’accouchement. 
		    Il offre un répit et permet d’éviter le surmenage et le dépassement du seuil de détresse psychologique. Il favorise le mieux-être, 
		    apporte du réconfort et une plus grande confiance en soi et contribue en fait à développer les capacités parentales. 
		    Il améliore l’organisation quotidienne et fonctionnelle liée aux responsabilités familiales et assure que le domicile reste un environnement 
		    sain et sécuritaire dans lequel la famille se sent bien. Il aide à briser l’isolement et permet à la mère de partager avec un adulte durant
		    sa journée, souvent le seul avec qui elle sera en contact. Il permet de faire la connaissance d’un nouveau milieu d’entraide et de ressources 
		    disponibles que représente le centre de ressources périnatales où la mère trouvera une multitude d’autres services et activités. Il favorise les
		     liens mère-enfant, encourage les liens familiaux, prend soin de la fratrie, dans cette période stratégique pour le couple. Il contribue à améliorer
		     la santé et le bien-être de tous les membres de la famille. Que demander de plus?Lucie Thibodeau, directrice du Réseau des centres de ressources 
		     périnatales
		     
		     <p><gras>Vous avez le goût de recevoir cette aide précieuse ?</gras>À Ressources-Naissances, il y a une équipe d’assistante à domicile qui 
		     offre des relevailles, composez le 834-8085 et expliquez vos besoins à la coordonnatrice, Mireille Bourgault.
		     
